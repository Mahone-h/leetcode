class Solution {
public:
    bool row[9][9];
    bool col[9][9];
    bool box[9][9];

    void solveSudoku(vector<vector<char>> &board) {
        int val, box_pos;
        for (int i = 0; i < 9; ++i) {
            for (int j = 0; j < 9; ++j) {
                if (board[i][j] == '.') {
                    continue;
                }
                val = board[i][j] - '1';
                box_pos = i / 3 * 3 + j / 3;
                if (!row[i][val] && !col[j][val] && !box[box_pos][val]) {
                    row[i][val] = col[j][val] =  box[box_pos][val] = true;
                }
            }
        }
        dfs(board,0);
    }

    bool dfs(vector<vector<char>> &board,int d) {
        if (d==81) return true;
        int i = d / 9, j = d % 9;
        if (board[i][j]!='.') return dfs(board, d + 1);
        int box_pos;
        for (int k = 0; k < 9; k++) {
            box_pos = i / 3 * 3 + j / 3;
            if (!row[i][k] && !col[j][k] && !box[box_pos][k]) {
                row[i][k] = col[j][k] =  box[box_pos][k] = true;
                board[i][j] = k + '1';
                if (dfs(board,d+1)) {
                    return true;
                } else {
                    board[i][j] = '.';
                    row[i][k] = col[j][k] =  box[box_pos][k] = false;
                }
            }
        }
        return false;
    }


};
