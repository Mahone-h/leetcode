    void push(int val) {
        x_stack.push(val);
        if (m_stack.empty() || val <= m_stack.top()) {  //=å¿…æœ‰
            m_stack.push(val);
        }
    }

    void pop() {
        if (x_stack.top() == m_stack.top()) {
            m_stack.pop();
        }
        x_stack.pop();
    }

    int top() {
        return x_stack.top();
    }

    int getMin() {
        return m_stack.top();
    }
